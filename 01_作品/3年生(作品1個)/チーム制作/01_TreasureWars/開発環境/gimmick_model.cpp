//==============================================================================================
// コンストラクタ
//==============================================================================================
CGimmick_Model::CGimmick_Model()
{
}

//==============================================================================================
// デストラクタ
//==============================================================================================
CGimmick_Model::~CGimmick_Model()
{
}

//==============================================================================================
// 初期化処理
//==============================================================================================
HRESULT CGimmick_Model::Init()
{
	CTrolley::Init();

	return S_OK;
}

//==============================================================================================
// 終了処理
//==============================================================================================
void CGimmick_Model::Uninit()
{
	CTrolley::Uninit();
}

//==============================================================================================
// 更新処理
//==============================================================================================
void CGimmick_Model::Update()
{
	CTrolley::Update();
}

//==============================================================================================
// 描画処理
//==============================================================================================
void CGimmick_Model::Draw()
{
	CTrolley::Draw();
}

//==============================================================================================
// 生成処理
//==============================================================================================
CGimmick_Model *CGimmick_Model::Create(D3DXVECTOR3 pos, int index,const char *Xfilename)
{
	CGimmick_Model *pGimmick_Model = nullptr;

	pGimmick_Model = new CGimmick_Model;

	if (pGimmick_Model != nullptr)
	{
		pGimmick_Model->SetPos(pos);
		pGimmick_Model->SetTrolley_Index(index);
		pGimmick_Model->Init();
		pGimmick_Model->LoadFileGimmick(Xfilename);
	}

	return pGimmick_Model;
}