#include"result_char.h"

//==============================================================================================
// コンストラクタ
//==============================================================================================
CResult_Char::CResult_Char()
{

}

//==============================================================================================
// デストラクタ
//==============================================================================================
CResult_Char::~CResult_Char()
{

}

//==============================================================================================
// 初期化処理
//==============================================================================================
HRESULT CResult_Char::Init()
{
	CCharacter::Init();

	SetMotion(MOTION_NEUTRAL);

	return S_OK;
}

//==============================================================================================
// 終了処理
//==============================================================================================
void CResult_Char::Uninit()
{
	CCharacter::Uninit();
}

//==============================================================================================
// 更新処理
//==============================================================================================
void CResult_Char::Update()
{
	CCharacter::Update();

	// モーション番号の設定
	ChangeMotion();

	// モーション
	Motion();

}

//==============================================================================================
// 描画処理
//==============================================================================================
void CResult_Char::Draw()
{
	CCharacter::Draw();
}

//==============================================================================================
// 生成処理
//==============================================================================================
CResult_Char *CResult_Char::Create(D3DXVECTOR3 pos, int num)
{
	CResult_Char *pResult_Char = nullptr;

	pResult_Char = new CResult_Char;

	if (pResult_Char != nullptr)
	{
		pResult_Char->SetIndex(num);
		pResult_Char->Init();
	}

	return pResult_Char;
}